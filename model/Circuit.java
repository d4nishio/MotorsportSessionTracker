package model;

public class Circuit {
	
	private int circuitID;
	private String name;
	
	public Circuit()
	{
		
	}
	
	public int getCircuitID() {
		return circuitID;
	}
	public void setCircuitID(int circuitID) {
		this.circuitID = circuitID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}
